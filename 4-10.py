animals = ["pandas", "crows", "black labs", "panthers", "elephants"]

print("The first three animals in the list are:\n")

for animal in animals[0:2]:
    print(animal)

print("Three animals from the middle of the list are:\n")
    
for animal in animals[1:4]:
    print(animal)
    
print("The last three animals in the list are:\n")

for animal in animals[2:]:
    print(animal)